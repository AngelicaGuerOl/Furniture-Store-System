    private void btn_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_actualizarActionPerformed
        // TODO add your handling code here:
        if(text_nombre.getText().isEmpty()||text_apellidos.getText().isEmpty()||text_telefono.getText().isEmpty()
            ||text_colonia.getText().isEmpty()||text_calle.getText().isEmpty()||text_nexterior.getText().isEmpty()||text_cp.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Complete todos los campos");
        }else{
            Cliente cliente=new Cliente();
            CtrlCliente ctCliente=new CtrlCliente();
            cliente.setId_cliente(id_cliente);
            cliente.setNombre_cliente(text_nombre.getText().trim());
            cliente.setApellidos_cliente(text_apellidos.getText().trim());
            cliente.setTelefono_cliente(text_telefono.getText().trim());
            cliente.setColonia_cliente(text_colonia.getText().trim());
            cliente.setCalle_cliente(text_calle.getText().trim());
            cliente.setNumero_ext_cliente(text_nexterior.getText().trim());
            cliente.setCodigo_postal_cliente(text_cp.getText().trim());
            if(ctCliente.actualizar(cliente)){
                JOptionPane.showMessageDialog(null, "Cliente actualizado");
                this.cargarTabla();
                this.limpiar();
            }else{
                 JOptionPane.showMessageDialog(null, "Fallo la actualizacion de cliente");
            }
        }
    }//GEN-LAST:event_btn_actualizarActionPerformed

    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        // TODO add your handling code here:
        CtrlCliente ctCliente=new CtrlCliente();
        if(is_selected){
            int opcion = JOptionPane.showConfirmDialog(this, "¿Estás seguro de eliminar este usuario seleccionado?",   "Confirmar Eliminación", JOptionPane.YES_NO_OPTION);
            if(opcion==JOptionPane.YES_OPTION){
                if(ctCliente.eliminar(this.id_cliente)){
                    JOptionPane.showMessageDialog(null, "El cliente seleccionado se eliminó correctamente");
                    this.cargarTabla();
                    this.limpiar();
                }else{
                    JOptionPane.showMessageDialog(null, "El cliente no se eliminó");
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione el cliente que desea eliminar");
       
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

