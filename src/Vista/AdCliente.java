    public void cargarTabla(){
        Connection cn=Conexion.Conectar();
        String query="SELECT * FROM cliente";
        try (PreparedStatement st = cn.prepareStatement(query)) {
            ResultSet rs = st.executeQuery();
            while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
          scp_adcliente.setViewportView(tb_adcliente);
          while (rs.next()) {
            Object fila[] = new Object[8];
            for (byte i = 0; i < 8; i++) {
                fila[i] = rs.getObject(i + 1);
            }
            modelo.addRow(fila);
        } 
          cn.close();
        }catch(SQLException e){
            System.out.println("Fallo la consulta de cllientes: "+e);
        }
        is_selected=false;
    }
    private void cargarInformacionCliente(int idCliente){
        try{
            Connection cn=Conexion.Conectar();
            PreparedStatement pst=cn.prepareStatement("SELECT * FROM cliente WHERE id_cliente='"+idCliente+"'");
            ResultSet rs=pst.executeQuery();
            if(rs.next()){
                text_nombre.setText(rs.getString("nombre_cliente"));
                text_apellidos.setText(rs.getString("apellidos_cliente"));
                text_telefono.setText(rs.getString("telefono_cliente"));
                text_colonia.setText(rs.getString("colonia_cliente"));
                text_calle.setText(rs.getString("calle_cliente"));
                text_nexterior.setText(rs.getString("numero_ext_cliente"));
                text_cp.setText(rs.getString("codigo_postal_cliente"));
            }
            cn.close();
        }catch(SQLException e){
            System.out.println("Error al seleccionar cliente");
        }
    }
