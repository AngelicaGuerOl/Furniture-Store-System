    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // TODO add your handling code here:
        Usuario usuario=new Usuario();
        CtrlUsuario ctUsuario=new CtrlUsuario();
        String rol="";
        rol=cmb_rol.getSelectedItem().toString();
        if(text_nombre.getText().equals("")||text_apellidos.getText().equals("")||text_telefono.getText().equals("")||text_password.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Complete todos los campos");
        }else{
            if(rol.equalsIgnoreCase("Seleccione el rol")){
                JOptionPane.showMessageDialog(null, "Seleccione un rol");
            }
            else{
            usuario.setNombre_usuario(text_nombre.getText());
            usuario.setApellidos_usuario(text_apellidos.getText());
            usuario.setTelefono_usuario(text_telefono.getText());
            usuario.setPassword_usuario(text_password.getText());
            usuario.setRol_usuario(rol);
            if(ctUsuario.agregar(usuario)){
                JOptionPane.showMessageDialog(null, "Registro guardado");
                this.limpiar();
                this.cmb_rol.setSelectedItem("Seleccione el rol");
                
            }else{
                JOptionPane.showMessageDialog(null, "Error al guardar en la base de datos");
            }
        }
        }
    }//GEN-LAST:event_btn_guardarActionPerformed

