    private void cargarCategorias() {
        Connection cn = Conexion.Conectar();
        String query = "SELECT categoria FROM categorias";
        Statement st;
        try {
            st = cn.createStatement();
            ResultSet rs = st.executeQuery(query);
            cmb_categoria.removeAllItems();
            cmb_categoria.addItem("Seleccione la categoria del articulo");
            while (rs.next()) {
                cmb_categoria.addItem(rs.getString("categoria"));
            }
            st.close();
            cn.close();
        } catch (SQLException ex) {
            System.out.println("Error al cargar categorias");
        }
    }

    private int idCategoria() {
        String sql = "SELECT * FROM categorias WHERE categoria = '" + this.cmb_categoria.getSelectedItem() + "'";
        Statement st;
        try {
            Connection cn = Conexion.Conectar();
            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                obtenerIdCategoria = rs.getInt("id_categoria");
            }

        } catch (SQLException ex) {
            System.out.println("Error al obtener id categoria");
        }
        return obtenerIdCategoria;
    }
