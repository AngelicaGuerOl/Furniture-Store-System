    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // TODO add your handling code here:
        Articulos articulo = new Articulos();
        CtrlArticulos ctArticulos = new CtrlArticulos();
        String categoria = cmb_categoria.getSelectedItem().toString();

        if (text_nombre.getText().equals("") || text_descripcion.getText().equals("") || text_condicion.getText().equals("")
                || text_color.getText().equals("") || text_precio.getText().equals("") || text_stock.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Complete todos los campos");

        } else {
            if (categoria.equalsIgnoreCase("Seleccione la categoria del articulo")) {
                JOptionPane.showMessageDialog(null, "Seleccione una categoria");
            } else {
                this.idCategoria();
                articulo.setId_categoria(obtenerIdCategoria);
                articulo.setNombre_articulo(text_nombre.getText());
                articulo.setDescripcion_articulo(text_descripcion.getText());
                articulo.setCondicion_articulo(text_condicion.getText());
                articulo.setColor_articulo(text_color.getText());
                String precioTxt = text_precio.getText();
                try {
                    double precio = Double.parseDouble(precioTxt);
                    articulo.setPrecio_articulo(precio);
                } catch (NumberFormatException e) {
                    System.err.println("Error al analizar el precio: " + e.getMessage());
                    return;
                }
                articulo.setStock_articulo(Integer.parseInt(text_stock.getText()));
                if (ctArticulos.agregar(articulo)) {
                    JOptionPane.showMessageDialog(null, "Registro guardado");
                    this.cargarCategorias();
                    this.cmb_categoria.setSelectedItem("Seleccione la categoria del articulo");
                    this.limpiar();

                } else {
                    JOptionPane.showMessageDialog(null, "Error al guardar");
                }
            }
        }
    }//GEN-LAST:event_btn_guardarActionPerformed

